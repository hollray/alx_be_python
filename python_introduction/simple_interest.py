principal = 1000  # Principal amount in dollars
rate = 0.05  # Annual interest rate (5%)
time = 3  # Expressed in Years
# Calculate simple interest using the formula: I = P * r * t
interest = principal * rate * time  # Interest earned in dollars
print("The Simple Interest is: $", interest)
